
/* sadly, x86 doesn't give us our interrupt number, so we need to push it ourselves.
   This means having many stub functions, for which this is a template */
#define ISR(num, errcode) \
	extern void isr ## num () ; \
	asm ( \
		".globl isr" #num "\n" \
		"isr" #num ":\n\t" \
			 errcode \
			"pushl $" #num "\n\t" \
			"jmp isr_stub\n\t");

